function getWindArrowEmoji(direction) {
    // 1. Handle missing data
    if (direction === null || direction === undefined) return "üí®";

    // 2. Handle Degrees (if the API sends a number like 180, 270)
    if (typeof direction === 'number') {
        const val = Math.floor((direction / 22.5) + 0.5);
        const arr = ["N", "NNE", "NE", "ENE", "E", "ESE", "SE", "SSE", "S", "SSW", "SW", "WSW", "W", "WNW", "NW", "NNW"];
        direction = arr[(val % 16)];
    }

    // 3. Map Compass String to Arrow Emoji
    const map = {
        "N": "‚¨áÔ∏è",   "NNE": "‚¨áÔ∏è", "NE": "‚ÜôÔ∏è",  "ENE": "‚ÜôÔ∏è", // Wind from North flows Down/South
        "E": "‚¨ÖÔ∏è",   "ESE": "‚¨ÖÔ∏è", "SE": "‚ÜñÔ∏è",  "SSE": "‚ÜñÔ∏è", // Wind from East flows Left/West
        "S": "‚¨ÜÔ∏è",   "SSW": "‚¨ÜÔ∏è", "SW": "‚ÜóÔ∏è",  "WSW": "‚ÜóÔ∏è", // Wind from South flows Up/North
        "W": "‚û°Ô∏è",   "WNW": "‚û°Ô∏è", "NW": "‚ÜòÔ∏è",  "NNW": "‚ÜòÔ∏è"  // Wind from West flows Right/East
    };

    // Note: Weather arrows usually point "Where the wind is going", 
    // so a North wind (From North) points Down (South).
    
    return map[direction.toUpperCase()] || "üí®";
}

